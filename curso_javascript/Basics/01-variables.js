// var --> forma original para declarar variables, no es recomendable
var helloWorld = "Hola Javascript"
console.log(helloWorld)

helloWorld = "hola de nuevo JS"
console.log(helloWorld)

// let --> Puede ser modificado el valor
let userName = "Carla"
console.log(userName)

userName = "Ya no me llamo Carla"
console.log(userName)

// const --> NO puede ser modificado el valor

const helloPeople = "Hola Gente!"
console.log(helloPeople)